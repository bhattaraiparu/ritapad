    <div class="collapsible-body">
      <table class="striped">
        <tbody>
          <tr>
            <td>Cause A:</td>
            <td>Surface of the part to be imaged is contaminated causing inK to be repelled. </td>
          </tr>
          <tr>
            <td>Solution A:</td>
            <td>	Clean the surface of the pad with Acetone or other suitable solvent to remove contamination. To test for surface  contamination : Check the tension level of the substrate with dyne solution, then wipe the surface on the substrate with Acetone and a smooth cloth. Recheck the surface tension of the substrate. If the surface tension numbers are not the same then some contamination is present from plasticizers, stabilizers, or extruding aids. </td>
          </tr>
          <tr>
            <td>Cause B:</td>
            <td>Ink is too thin. </td>
          </tr>
          <tr>
            <td>Solution B:</td>
            <td>Add fresh ink to the closed cup and adjust the viscosity of the ink solution. </td>
          </tr>

          <tr>
            <td>Cause C:</td>
            <td>The depth of etching is more </td>
          </tr>
          <tr>
            <td>Solution C:</td>
            <td>Use proper etched plate. Depth of etching should be 15~25 m m (Depending upon substrate and print.</td>
          </tr>
          <tr>
            <td>Cause D:</td>
            <td>Ink mixture contains too much retarder, preventing ink from becoming tacky. </td>
          </tr>
          <tr>
            <td>Solution D:</td>
            <td>Decrease the speed of machine from image pickup to transfer, this will allow ink to be exposed to the air longer before transfer which will increase its tackiness. Mix new ink with a faster solvent if, necessary.  </td>
          </tr>
          <tr>
            <td>Cause E:</td>
            <td>Ink was left on the Pad during set up. </td>
          </tr>
          <tr>
            <td>Solution E:</td>
            <td>Keep Pad clean during set up. Use adhesive tape to remove ink from the pad. Do not allow ink to dry on pad. </td>
          </tr>
          <tr>
            <td>Cause F:</td>
            <td>Timing of image pick up to part transfer is too slow </td>
          </tr>
          <tr>
            <td>Solution F:</td>
            <td>Increase timing speed of the machine from image pick up to the point of image transfer to the part. Increase with caution to maintain good print quality and prevent pad wiggle. </td>
          </tr>
          <tr>
            <td>Cause G:</td>
            <td>Insufficient pad pressure causing incomplete image on the product. </td>
          </tr>
          <tr>
            <td>Solution G:</td>
            <td>Increase pad pressure on the product side upto complete image transfer but the pressure should be minimum. </td>
          </tr>
          <tr>
            <td>Cause H:</td>
            <td>Pad is beyond its life. </td>
          </tr>
