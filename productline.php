<h3 class="sub-title">MDM-100D-GF</h3>

<p class="text">The machine MDM-100D-GF single and double colour pad printing machine is based on an entirely new concept. By using two transfer pads mounted on vertical tower which rotates alternatively in clock-wise and counter clock-wise direction it is possible to print two colours with only one stationary rotary fixture. This design also allows printing of images on two different part levels by two independently control pneumatic pad cylinders.</p>

<h3 class="text"><b>Main Features and Benefits</b></h3>

<ul class="decimal">
  <li>Two colour printing can be done with only one fixture which is stationery.</li>
  <li>Each of the two colours can be independently double printed for more opacity.</li>
  <li>Two independently operated pad cylinders allow printing on two different part levels.</li>
  <li>Specially designed index units results in smooth and accurate pad rotating motion and high two colour registration accuracy.</li>
</ul>

