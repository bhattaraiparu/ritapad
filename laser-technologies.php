    <h3 class="sub-title">Acousto -Optic q Switching (Pulsing The Lasee Beam)</h3>

    <p class="text">When an RF signal is applied to the transducer an acoustic wave is projected through the quartz which momentarily compresses the material and changes its index of refraction. Some of the light passing through the Q-switch is diffracted to a small angle and misses the rear mirror momentarily, causing the lasing action to cease. During this phase, the atoms in the laser rod gain energy which is stored until the RF signal is removed. The resulting burst of laser energy may be several kilowatts of peak power, significantly more peak power than the laser would emit without the Q-switch. Q-switching is the most common means of producing very short pulse widths and very high peak power from a relatively low power Nd:YAG laser. <em>Q-switches at Rita Laser and globally typically operate at frequencies from 1 to 50 kHz.</em></p>

    <h3 class="sub-title">LASER POWER - A MACHINE SPECIFICATION</h3>

    <p class="text"><b>Average Power and Power Density:</b> A continuous wave (CW) Nd:YAG laser marker rated at 50 watts produces the same average power as a 50 watt light bulb. The difference is the light bulb projects white light of all wavelengths in all directions. The laser emission is coherent (traveling in one direction), mono-chromatic (of one wavelength) and in phase. The power density, expressed in watts/cm2 tells us how concentrated the laser energy is at the point of focus. A laser producing 50 watts of power in a 0.005" diameter spot will produce considerably higher power density than the same laser with a 0.500" spot diameter. The effective marking power of the laser is the net power density produced at the point of focus or where the best laser mark is achieved .<em> Rita Laser produces Marking Lasers with Powers ranging from 30 to 150 Watts, with the highest Flux Ratings for Longest Possible Machine Life (LPML)</em> </p>

    <h3 class="sub-title">GALVANOMETER SCANNING (BEAM STEERING) - HOW A MARKING LASER "WRITES"</h3>

    <img class="responsive-img" src="images/Galvanometer.gif" alt="Galvanometer">
    <p class="text"><b>Galvanometer or XY Scanner:</b> The output of the laser marker is typically directed by moving mirrors operating in both the X and Y planes. The XY mirror sets are each independently connected to highly accurate, repeatable and extremely fast galvanometer drivers (motors). Precision beam steering, and thus the laser mark, in both the X and Y axis is controlled by the software and control electronics.</p>

    <h3 class="sub-title">Marking Field</h3>
    <p class="text"><b>Flat Field Lens:</b> The most common method to focus the laser beam in a laser marker is after it is positioned by the XY galvanometer mirrors by using a flat field lens. Typical flat field lens sets provide marking areas from 60 mm to over 300 mm in diameter. These flat field lenses provide a relatively large marking area and permit fast, high quality laser marks and distortion free pictures to be made on a broad range of metals and other materials.</p>

