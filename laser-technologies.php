    <h3 class="sub-title">Optical Characteristics of CO 2 & Nd:YAG Lasers</h3>

    <p class="text"><b>Electro-magnetic Spectrum:</b> Nd:YAG and Carbon Dioxide lasers emit radiation at different wavelengths. Fundamentally, the 1.064 micron wavelength of the Nd:YAG is an order of magnitude shorter than the 10.6 micron radiation from a Carbon Dioxide laser. In materials processing, the shorter wavelength of the Nd:YAG couples better to metal while the longer Carbon Dioxide wavelength is more suitable for cutting plastics, ceramics and other organic materials. The electro-magnetic spectrum below shows where the Nd:YAG and CO2 laser emissions are located relative to other lasers and radiation sources.</p>

    <img class="responsive-img" src="images/wavelenth.gif" alt="wavelenth">

    <p class="text">The fundamental wavelength of the Nd:YAG laser is 1.064 microns while the carbon dioxide laser emits at 10.6 microns. Most industrial <a href="laser-applications.php">laser markers</a> use either the fundamental output of these lasers. The Nd:YAG laser wavelength can also be shifted into the green at 0.355 micron or the UV at 0.266 microns using non linear optics. These shorter wavelengths are now being adopted for very small (micro-marking) applications in a broad range of materials. The shorter wavelength of the Nd:YAG laser couples to metal better than the CO2 laser. However, for many marking applications on plastics or painted objects, the CO2 laser is equally as practical as the Nd:YAG laser and may be somewhat less costly. Nd:YAG laser beams also have the advantage of focusing to a smaller spot diameter, with higher power density. As a result, they produce a smaller heat affected zone and less thermal distortion. Nd:YAG laser energy may also be delivered via a fiber optic cable which allows for greater flexibility in the factory environment </p>

    <p class="text">In general, RITA LASER produced Nd:YAG lasers at the fundamental 1.064 wavelength are best suited for marking metals while the CO2 laser is more suited for plastics, painted or organic marking. This is the Global Standard.</p>

