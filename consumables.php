<h3 class="sub-title">Thinners & Retarders</h3>

<p class="text">These auxiliary agents are used to adjust the viscosity of ink to the proper printing consistency in different type of environment and requirements. Its type and amount depends on speed of machine, environment, cliche depth etc.</p>

<p class="text">Retarders are used for slow printing speeds . In many applications it is better not to use them alone but add them to universal thinners. Evaporation rate of the thinner/ retarder depends on the solubility of the ink. Thinners with good solubility are retained in the ink for a longer period of time than thinner with a low solubility.</p>

