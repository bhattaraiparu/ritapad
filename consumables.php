<h3 class="sub-title">Pads</h3>
<p class="text">Rita Pad offers more than hundred of standard shape pad, available in different qualities and hardness to suits industrial pad printing applications. Special pads for specific requirement can be developed and manufactured on request. The pad is certainly an important and critical element in pad printing. It takes the ink from plate and deposits it on the object to be printed. It is recommended for printing graphics with particularly precise printing.
The ideal contact angle between object and pad surfaces lies between 20 ° and 50 ° . The point of the pad is always critical area, especially where engraving is not screened. In general the point of the Pad must always find itself outside engraving</p>

<p class="text">New pad need to be tamped. This can be done by using Acetone dampened cotton and tamping 100- 200 strokes (up down) of pad. Now the pad is activated and ready for use. Do not use any other thinner for tamping of pad because other thinner will rapidly damage the pad surface. Pads should be always stored far from heat sources and in closed cabinet in order to avoid contact with dust and light</p>

